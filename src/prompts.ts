/** Builds the per-request prompt for a quick command. */
export function buildCommandPrompt(command: string, location: string): string {
  switch (command) {
    case 'cinema':
      return `Найди сеансы в кинотеатрах г. ${location} на сегодня/завтра. Выбери только под мой профиль.`;
    case 'releases':
      return `Найди фильмы/сериалы, вышедшие в цифре (WEB-DL, Stream) за последние 30 дней. Исключи CamRip. Только под мой вкус.`;
    case 'soon':
      return `Что выйдет в мире в ближайшие 3 месяца? Только главные хиты под мой вкус.`;
    default:
      return command; // free-text query
  }
}
